#include "unistd.h"

int			main(void)
{
	char	c;

	while (read(0, &c, 1) > 0)
	{
		if (c == 'O')
			write(1, "\x1b[31m",6);
		else if (c == 'X')
			write(1, "\x1b[34m", 6);
		write(1, &c, 1);
		if (c == 'O' || c == 'X')
			write(1, "\x1b[0m",5);
	}
	return (0);
}
